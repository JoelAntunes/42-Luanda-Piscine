#include <unistd.h>

void	ft_is_negative(int n)
{
	char	neg;
	char	pos;

	if (n < 0)
	{
		neg = 'N';
		write(1, &neg, 1);
	}
	else
	{
		pos = 'P';
		write(1, &pos, 1);
	}
}
